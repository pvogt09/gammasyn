function [is] = isdiag(X)
	%ISDIAG Determine whether a matrix is diagonal.
	%   ISDIAG(X) returns true if X is a diagonal matrix. Otherwise,
	%   it returns false.
	%
	%   See also ISTRIL, ISTRIU, ISBANDED, DIAG, TRIL, TRIU, BANDWIDTH.

	%   Copyright 2013 The MathWorks, Inc.
	%   Built-in function.
	is = isequal(diag(diag(X)), X);
end