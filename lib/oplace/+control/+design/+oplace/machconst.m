function DPMDC = machconst(K)
%******************************************************************
%  DIESES UNTERPROGRAMM LIEFERT DOPPELT LANGE MASCHINENABHAENGIGE
%  KONSTANTEN, DIE JE NACH EINGANGSINDEX K FOLGENDE BEDEUTUNG HABEN:
%    K=1 : KLEINSTE POSITIVE ZAHL ETA SO, DASS -ETA EXISTIERT
%    K=2 : QUADRATWURZEL AUS 1.001*ETA
%    K=3 : KLEINSTE POSITIVE ZAHL EPS SO, DASS 1.+EPS>1.
%          UND 1.-EPS<1. IST.
%    K=4 : QUADRATWURZEL AUS 0.999*EPS
%    K=5 : GROESSTE POSITIVE ZAHL BIG SO, DASS -BIG EXISTIERT
%    K=6 : QUADRATWURZEL AUS 0.999*BIG
%  BEIM UEBERGANG AUF EINE ANDERE RECHENMASCHINE HABEN DIESE KONSTANTEN
%  WEGEN DER UNTERSCHIEDLEICHEN ZAHLENLAENGEN ANDERE WERTE. VON DEN
%  FOLGENDEN DATA-ANWEISUNGEN IST DIEJENIGE GUELTIG, DIE KEIN C IN
%  DER SPALTE 1 HAT.
%*****************************************************************
%      DOUBLE PRECISION ETA,EPS,BIG
%
% ***   UNIVAC 1106/1108   ***
%
%      DATA ETA/1.2D-308/,EPS/1.734723476D-18/,BIG/8.988D+307/
%
% ***   IBM 360, IBM 370   ***
%
%     DATA ETA/Z0010000000000000/,EPS/Z3410000000000000/,
%    1     BIG/Z7FFFFFFFFFFFFFFF/
%
% ***   BURROUGHS   ***
%
%     DATA ETA/01771000000000000,07770000000000000/,
%    1     EPS/01451000000000000,00000000000000000/,
%    2     BIG/00777777777777777,07777777777777777/
%
%
% ***   DEC 11   ***
%
%     DATA ETA/2.938735878D-39/, EPS/2.775557562D-17/,BIG/1.7D+38/
%
%
% ***   IBM PC MIT 8087   ***
%
%
%-----------------------------------------------------------------------
%

ETA=4.19D-307;
EPS=2.220446D-16;
BIG=1.67D308;

switch K
	case 1
		DPMDC=ETA;
	case 2
		DPMDC=sqrt(1.001*ETA);
	case 3
		DPMDC=EPS;
	case 4
		DPMDC=sqrt(0.999*EPS);
	case 5
		DPMDC=BIG;
	case 6
		DPMDC=sqrt(0.999*BIG);
	otherwise
		error('Index greater 6 in MACHCONST');
end